#!/usr/bin/env node

import { resolveWorktreeEnv } from "./lib/worktree-env.mjs";

const allowedKeys = [
  "WORKTREE_ID",
  "WORKTREE_OFFSET",
  "COMPOSE_PROJECT_NAME",
  "POSTGRES_USER",
  "POSTGRES_PASSWORD",
  "POSTGRES_DB",
  "POSTGRES_PORT",
  "REDIS_PORT",
  "REDIS_DB",
  "REDIS_HOST",
  "REDIS_URL",
  "DATABASE_URL",
  "TEST_DATABASE_URL",
  "PORT",
  "API_PORT",
  "API_URL",
  "GAME_SERVER_PORT",
  "GAME_PORT",
  "GAME_WS_URL",
  "WEB_PORT",
  "WORKER_PORT",
  "E2E_BASE_URL",
  "PLAYWRIGHT_BASE_URL",
  "NEXT_PUBLIC_LIVE_ENDPOINT",
  "TEST_HOST",
];

function shellQuote(value) {
  return `'${String(value).replaceAll("'", `'"'"'`)}'`;
}

const resolved = resolveWorktreeEnv();

console.log("# Generated by scripts/worktree-up. Ignored by Git.");
for (const key of allowedKeys) {
  const value = resolved[key];
  if (value !== undefined && value !== "") {
    console.log(`export ${key}=${shellQuote(value)}`);
  }
}
